// 2. Check the type of different variables using `typeof`.
// 3. Store your mobile number in a variable and check its type.
// 4. Create a variable with value `null` and check its type.
// 5. Create a bigint number and print it.

// Question 1 - Create variables of type string, number, boolean, null, and undefined.

var name = "Aditya";
var age = 18;
var isLogin = true;
var empty = null;
var nothing;

// Question 2 - Check the type of different variables using `typeof`.

console.log(name, typeof name);
console.log(age, typeof age);
console.log(isLogin, typeof isLogin);
console.log(empty, typeof empty);
console.log(nothing, typeof nothing);

// Question 3 - Store your mobile number in a variable and check its type.

var number = 1234567890;

console.log(number, typeof number);

// Question 4 - Create a variable with value `null` and check its type.

var no = null;

console.log(no, typeof no);

// Question 5 - Create a bigint number and print it.

var largeNum = 90071992547409912348998n;

console.log(largeNum);